#рЃерЃћрЃАрЃљрЃФрЃџрЃћрЃЉрЃћрЃџрЃўрЃљ рЃњрЃљрЃЏрЃЮрЃДрЃћрЃюрЃћрЃЉрЃљ list-рЃќрЃћ, string-рЃќрЃћ рЃЊрЃљ рЃАрЃ«рЃЋрЃљ iterable-рЃћрЃЉрЃќрЃћ.

